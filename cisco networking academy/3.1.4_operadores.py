# Operadores de comparación: mayor o igual que
"""

El operador mayor que tiene otra variante especial, una variante no escrita, pero se denota de manera diferente que la notación aritmetica clásica: >= (mayor o igual que).

Hay dos signos  subsecuentes, no uno.
Ambos operadores (escritos y no escritos), así como los otros dos que se analizan en la siguiente sección, son operadores binarios con enlazado de lado izquierdo, y su proridad es mayor que la mostrada por == y !=.

Si queremos saber si tenemos que usar un gorro o no, nos hacemos la sigueinte pregunta:
centigrade_outside >= 0.0 # Mayor o igual que
"""

