# Argumento end
"""

Por defecto, la función print() en Python termina con un salto de línea (\n), lo que significa que cada vez que llamas a print(), el contenido aparece en una nueva línea.

El argumento end te permite cambiar lo que se imprime al final de cada llamada a print()

Sintaxis:
print(objeto, end = "carácter_que_quieras")

Ejemplo: 
print("hola", end = " ")
print("mundo")
hola mundo --> Salida por consola, 2 cadenas de diferentes funciones print() en una sola línea
Nota: El primer print() no termina con un salto de línea, si no con un espacio, gracias a end = " ".
"""



